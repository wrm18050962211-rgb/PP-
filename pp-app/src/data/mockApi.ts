function order(input: Omit<AppOrder, 'statusText' | 'amountText' | 'steps' | 'currentStep'>): AppOrder {
  const steps = getOrderSteps(input.status);
  return {
    ...input,
    statusText: orderStatusText[input.status],
    amountText: formatMoney(input.amountCents),
    ...steps,
  };
}

